void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	b;

	i = 0;
	while (i < (size / 2))
	{
		b = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = b;
		i++;
	}
}
